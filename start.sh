#!/bin/bash

# Получаем порт от Render (или используем 10000 по умолчанию)
PORT=${PORT:-10000}

echo "Запускаем бэкенд FastAPI на порту $PORT..."
# Запускаем uvicorn в фоновом режиме (знак & в конце обязателен)
uvicorn main:app --host 0.0.0.0 --port $PORT &

echo "Запускаем Telegram-бота..."
# Запускаем бота в фоновом режиме
python bot.py &

# Команда wait -n заставляет скрипт ждать.
# Если любой из процессов (бот или сервер) упадет с ошибкой,
# скрипт завершится, и Render автоматически перезапустит весь контейнер.
wait -n

# Возвращаем код ошибки упавшего процесса
exit $?
